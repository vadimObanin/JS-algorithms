// 1. Using for-------------------------------------------------------------------------
function searchNumber(arr, num) {
  let j = 0;
  for (let i = 0; i < arr.length; i++) {
    if (arr[i] === num) {
      return `Цифра ${num} находится на позиции ${i}`;
    }
    j++;
  }
  if (j === arr.length) {
    console.log("Искомый элемент отсутствует в массиве");
  }
}

const mainArray = [2, 4, 5, 0, 9, 8, 20];
console.log(searchNumber(mainArray, 4));



